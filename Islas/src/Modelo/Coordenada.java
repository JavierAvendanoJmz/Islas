package Modelo;

/**
 *
 * @author j4v13
 */
public class Coordenada {
    
    int f;
    int c;

    public Coordenada(int f, int c) {
        this.f = f;
        this.c = c;
    }

    public int getF() {
        return f;
    }

    public void setF(int f) {
        this.f = f;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
               
}
